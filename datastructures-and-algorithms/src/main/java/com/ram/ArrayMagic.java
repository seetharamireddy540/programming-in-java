package com.ram;

public class ArrayMagic {

    public static void main(String[] args) {

        int array[] = new int[100]; // 0 ... 99
        int size  = array.length;
        System.out.println("Array size - " + size);

        System.out.println("90%2 = " + (90%2)); // Reminder
        System.out.println("90/2 = " + (90/2)); // Quotient
    }
}
